# Ciclo de repeteción while do, trabaja asi:
# while condición : debe ser V o falsa, si se cumple la condición
#                   si se cumple la condición sigue aqui
#                   si no se cumple sigue aqui
# Presente en pantalla los números del 1 al 10

numero = 1
while numero <= 10:
    print(numero)  # 1,2,3,...,10
    numero = numero + 1
print("Fin")


