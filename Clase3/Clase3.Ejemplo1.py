# Quiero leer un número e imprimirlo en pantalla, debo saber si es entero o real
# Si es entero (sin coma) va con int
# Si es real (con coma) va con float
# Ingresamos un número entero y lo presentamos en pantalla
edad = int(input("Ingrese su edad: "))
print(edad)
# Ingresamos un numero real y lo presentamos en pantalla
